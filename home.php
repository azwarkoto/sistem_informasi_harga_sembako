<p class="text-black"><strong>Sistem Informasi Harga Sembako, merupakan sistem yang akan menampilkan beberapa 
    harga sembako. Setiap barang yang ada di Kios Pasar Horas Jaya Kota Pematangsiantar, sebagian 
    diantaranya merupakan sembako. Sembako tersebut akan dijual kepada Masyarakat. 
    Untuk mempermudah Masyarakat tentang sembako apa saja yang akan dijual pada setiap Kios
    yang ada di Pasar Horas Jaya Pematangsiantar, dengan adanya Sistem Informasi ini, kiranya dapat
    membantu Masyarakat dalam mengetahui hal tersebut.
</strong></p>
<p class="text-black"><strong>Sistem Informasi Harga Sembako Pada Pasar Horas Jaya Kota Pematangsiantar
    bertujuan untuk menginformasikan kepada Masyarakat tentang harga sembako yang ada pada Setiap
    Kios yang berada pada Pasar Horas Jaya Kota Pematangsiantar.
</strong> </p>
